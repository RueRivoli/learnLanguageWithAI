    → I can’t do it. Yürüyemezsin → You can’t walk. Burada sigara içemezsiniz →
    You can’t smoke here. This can mean inability or prohibition — context
    decides. 4. Expressing Uncertainty: “-mayabilir” There’s another, softer
    negative form: “-mayabilir/-meyebilir.” It expresses possibility of NOT
    doing something rather than complete inability. Formula: Verb stem + ma/me +
    y + a/e + bilir + personal ending Examples: Gelmeyebilirim → I might not
    come. Bilmeyebilirsin → You might not know. Yemek istemeyebilirim → I might
    not want to eat. This is more flexible than -emez/-amaz, which is absolute.
    5. Using “Ama” with “-abilir/-ebilir” The word “ama” means “but” and is
