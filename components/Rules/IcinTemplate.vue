    “Why?” or “For what?” Use “ne için” or its shortened form “niçin” (similar
    to “neden”). Examples: Ne için yaşıyorsun? → What are you living for?
    Kadınlar niçin topuklu ayakkabı giyiyor? → Why do women wear high heels? C.
    Yes/No Questions Attach “mi/mi/mu/mü” after için following vowel harmony.
    Examples: Sanat, sanat için mi? → Is art for art’s sake? Onun için mi bu
    kadar üzgünsün? → Is that why you’re so sad? Sadece mesaj yazmak için mi
    yazıyorsun? → Are you texting just for the sake of texting? 6. Quick
