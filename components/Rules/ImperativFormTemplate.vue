In English, when you say:

“Close the door!”

“Sit down!”

“Please listen!”

These are imperatives — direct statements telling someone to do or not do something.

Turkish works the same way, but the endings change based on who you’re talking to and how polite you want to sound.

2. Forming the Imperative
A. Informal “You” (Sen)

This is the form you use with friends, family, or people your age.
The rule is simple: use the bare verb stem — nothing else.

Verb	Meaning	Imperative (Sen)
Gelmek	To come	Gel! → Come!
Gitmek	To go	Git! → Go!
Beklemek	To wait	Bekle! → Wait!
Oturmak	To sit	Otur! → Sit!
Konuşmak	To speak	Konuş! → Speak!

Example Sentences

Buraya gel! → Come here!

Sessiz ol! → Be quiet!

Hemen başla! → Start right now!

B. Polite or Plural “You” (Siz)

When addressing strangers, elders, or groups of people, you add the ending -in or -iniz.

Verb	Meaning	Imperative (Siz)
Gelmek	To come	Gelin! → Come!
Gitmek	To go	Gidin! → Go!
Beklemek	To wait	Bekleyin! → Wait!
Oturmak	To sit	Oturun! → Sit!
Konuşmak	To speak	Konuşun! → Speak!

Example Sentences

Lütfen buraya gelin. → Please come here.

Yavaş konuşun. → Speak slowly.

Lütfen bekleyiniz. → Please wait. (Very polite)

Tip: Adding lütfen (please) softens the command and makes it sound more polite.

C. “Let’s…” (Biz)

When you want to include yourself in the action, Turkish uses -elim / -alım depending on vowel harmony.

Verb	Meaning	Imperative (Biz)
Gelmek	To come	Gelelim! → Let’s come!
Gitmek	To go	Gidelim! → Let’s go!
Başlamak	To start	Başlayalım! → Let’s start!
İzlemek	To watch	İzleyelim! → Let’s watch!
Oynamak	To play	Oynayalım! → Let’s play!

Example Sentences

Hadi gidelim! → Let’s go!

Başlayalım! → Let’s start!

Birlikte çalışalım. → Let’s work together.

D. Third Person Commands (O / Onlar)

When you want to tell someone else to do something, use -sin / -sın / -sun / -sün depending on vowel harmony.
For plural, add -lar / -ler.

Verb	Meaning	Imperative (O)	Imperative (Onlar)
Gelmek	To come	Gelsin → Let him/her come	Gelsinler → Let them come
Gitmek	To go	Gitsin → Let him/her go	Gitsinler → Let them go
Başlamak	To start	Başlasın → Let him/her start	Başlasınlar → Let them start

Example Sentences

Ayşe de gelsin. → Let Ayşe come, too.

Çocuklar oynasın. → Let the children play.

Herkes sessiz olsun. → Let everyone be quiet.

3. Negative Imperatives

Saying “Don’t do it” is just as simple. Use -me / -ma before the imperative ending.

Verb	Positive	Negative	English
Gitmek	Git!	Gitme!	Don’t go!
Konuşmak	Konuş!	Konuşma!	Don’t speak!
Beklemek	Bekle!	Bekleme!	Don’t wait!
Bakmak	Bak!	Bakma!	Don’t look!

Example Sentences

Buraya gelme! → Don’t come here!

Konuşmayın, lütfen. → Please don’t speak.

Ona dokunma! → Don’t touch it!

4. Quick Recap Table
Person	Ending	Example	Meaning
Sen (informal you)	Bare stem	Gel!	Come!
Siz (polite/plural you)	-in / -iniz	Gelin!	Come!
Biz (let’s)	-elim / -alım	Gidelim!	Let’s go!
O (he/she/it)	-sin / -sın / -sun / -sün	Gitsin!	Let him/her go!
Onlar (they)	-sinler	Gitsinler!	Let them go!
5. Tips to Sound Natural

Use lütfen to soften imperatives:
“Lütfen bekleyin.” → “Please wait.”

Tone matters. Imperatives can sound friendly or rude depending on how you say them.

Practice by giving small daily commands to yourself:
“Uyan!” → Wake up!
“Kahve yap!” → Make coffee!
    
