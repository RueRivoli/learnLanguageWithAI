    In English, when you say: “Close the door!” “Sit down!” “Please listen!”
    These are imperatives — direct statements telling someone to do or not do
    something. Turkish works the same way, but the endings change based on who
    you’re talking to and how polite you want to sound. 2. Forming the
    Imperative A. Informal “You” (Sen) This is the form you use with friends,
    family, or people your age. The rule is simple: use the bare verb stem —
    nothing else. Verb Meaning Imperative (Sen) Gelmek To come Gel! → Come!
    Gitmek To go Git! → Go! Beklemek To wait Bekle! → Wait! Oturmak To sit Otur!
    → Sit! Konuşmak To speak Konuş! → Speak! Example Sentences Buraya gel! →
    Come here! Sessiz ol! → Be quiet! Hemen başla! → Start right now! B. Polite
    or Plural “You” (Siz) When addressing strangers, elders, or groups of
    people, you add the ending -in or -iniz. Verb Meaning Imperative (Siz)
    Gelmek To come Gelin! → Come! Gitmek To go Gidin! → Go! Beklemek To wait
    Bekleyin! → Wait! Oturmak To sit Oturun! → Sit! Konuşmak To speak Konuşun! →
    Speak! Example Sentences Lütfen buraya gelin. → Please come here. Yavaş
    konuşun. → Speak slowly. Lütfen bekleyiniz. → Please wait. (Very polite)
    Tip: Adding lütfen (please) softens the command and makes it sound more
    polite. C. “Let’s…” (Biz) When you want to include yourself in the action,
    Turkish uses -elim / -alım depending on vowel harmony. Verb Meaning
    Imperative (Biz) Gelmek To come Gelelim! → Let’s come! Gitmek To go Gidelim!
