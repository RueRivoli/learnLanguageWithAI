  Want to sound natural and confident when describing where things are in Turkish?
Then it’s time to master postpositions—small but powerful words like içinde, yanında, and arkasında that help you paint a picture when you speak.

Unlike English, where we use prepositions (inside, behind, next to), Turkish places these location words after the noun. That’s why we call them postpositions.

1. How Postpositions Work

Formula:
Noun + Possessive Ending + Postposition

Example:

Evin içinde → “Inside the house”

Arabanın yanında → “Next to the car”

The possessive ending connects the noun to the postposition. Don’t worry—we’ll break it down step by step.

2. Essential Postpositions for Location

Here are the most common and useful ones:

Postposition	Meaning	Example	English
içinde	inside	Kutunun içinde	Inside the box
dışında	outside	Evin dışında	Outside the house
yanında	next to	Arabanın yanında	Next to the car
önünde	in front of	Okulun önünde	In front of the school
arkasında	behind	Binanın arkasında	Behind the building
üstünde / üzerinde	on top of	Masanın üstünde	On the table
altında	under	Sandalyenin altında	Under the chair
arasında	between	İki evin arasında	Between the two houses
karşısında	opposite / across	Parkın karşısında	Across from the park
3. The Possessive Connection

To use these correctly, the noun takes a possessive ending before the postposition. This shows “of what” the position refers to.

Here’s a quick guide using the noun ev (house):

Case	Suffix	Example	Meaning
Inside	-in	Evin içinde	Inside the house
Outside	-in	Evin dışında	Outside the house
In front of	-in	Evin önünde	In front of the house
Next to	-in	Evin yanında	Next to the house
Behind	-in	Evin arkasında	Behind the house

For plural nouns or vowel harmony, the endings adjust, but the rule stays the same.

4. Example Sentences You’ll Actually Use

Let’s put these into real-life contexts:

Turkish	English
Anahtar çantanın içinde.	The key is inside the bag.
Araba garajın dışında.	The car is outside the garage.
Kafe okulun yanında.	The café is next to the school.
Çocuk parkın önünde oynuyor.	The child is playing in front of the park.
Ayakkabılar kapının arkasında.	The shoes are behind the door.
Kitap masanın üstünde.	The book is on the table.
Kedi koltuğun altında uyuyor.	The cat is sleeping under the couch.
Postane hastane ile bankanın arasında.	The post office is between the hospital and the bank.
5. Tips to Sound Natural

Always attach the possessive suffix before the postposition.
✘ “Ev içinde” → sounds unnatural
✔ “Evin içinde” → correct

Be careful with vowel harmony.
Example:

Okul → okulun önünde

Park → parkın karşısında

Use these with the locative case if you want to specify where something is.
Example:

“Kitap masada.” (The book is on the table.)

“Kitap masanın üstünde.” (The book is on top of the table.)

